public class TechnicalEmployee extends Employee {
    static int checkInCounter = 0;
    int checkInCount = 0;

    public TechnicalEmployee (String name) {
        this.name = other.name;
        double baseSalary = 75000.00;
    }

    public String employeeStatus() {
        checkInCounter++;
        checkInCount = checkInCounter;
        return employeeID + " " + name + " has " + checkInCount + " successful check-ins.";
    }
}